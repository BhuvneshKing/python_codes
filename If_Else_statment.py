'''
This simple code example illustrates the use of an if-else statement. It simply checks whether a given number is greater than or less than 5.
'''
# Initializes the variable a with the value 10
a = 10

# If the value of the variable a is greater than 5, the string “a is greater than 5” is output
if a > 5:
    print("a is greater than 5")
# If this is not the case, the string “a is smaller than 5” is output    
else:
    print("a is smaller than 5")